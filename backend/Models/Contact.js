import mongoose from "mongoose";
const Contact_Schema = new mongoose.Schema(
  {
    location: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default mongoose.model("contact", Contact_Schema);
